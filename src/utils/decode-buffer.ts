/**
 * Decode string from buffer, in either CP437 or UTF8 encoding.
 */
export function decodeBuffer(buffer: Buffer, start: number, isUtf8: boolean): string {
	if(isUtf8) {
		return buffer.toString('utf8', start);
	}

	let str = '';

	for(let i = start; i < buffer.length; i++) {
		str += CP437_CHARS[buffer[i]];
	}

	return str;
}
